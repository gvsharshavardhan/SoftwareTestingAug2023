public class VariablesDemo {


    public static void main(String[] args) {

        //declaration of a variable
        int a;
        int b = 50;

        //initialize
        a = 20;

        System.out.println(a);//20
        System.out.println(b);//50

        double f = 20.6;
        System.out.println(f);

        char c = 'a';
        System.out.println(c);


        String  name = "busyqa";
        System.out.println(name);

    }
}
