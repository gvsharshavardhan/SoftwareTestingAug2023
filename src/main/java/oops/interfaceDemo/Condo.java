package oops.interfaceDemo;

public abstract class Condo implements Building {


    public void buildFoundation() {
        System.out.println("my condo has 10ft deep foundation");
    }


    public void buildPillars() {
        System.out.println("my condo has 20 pillars");
    }


    public void buildBeams() {
        System.out.println("my condo has 10 beams");
    }



}
